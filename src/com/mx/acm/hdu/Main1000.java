package com.mx.acm.hdu;

import java.util.Scanner;
/**
 *  杭电例题
 * @author maoxin
 *
 */
public class Main1000 {
	  
	public static void main(String[] args) {
		Scanner in = new Scanner(System.in);
		int a, b;
		while (in.hasNextInt()) {
			a = in.nextInt();
			b = in.nextInt();
			System.out.println(a + b);
		}
	}

}
